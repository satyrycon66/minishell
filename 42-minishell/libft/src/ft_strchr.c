#include "../include/libft.h"

char	*ft_strchr(const char *s, int c)
{
	int	i;

	i = 0;
	if (!s)
		return (NULL);
	while (s[i] != (char)c && s[i])
		i++;
	if (s[i] == (const char)c)
		return ((char *)&s[i]);
	else
		return (NULL);
}
